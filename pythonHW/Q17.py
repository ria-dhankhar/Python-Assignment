# Q) Write a program in python that converts a given string to title case (first letter of each word capitalized)

def convert_to_title_case():
    
    input_string = input("Enter a string: ")
    
    title_case_string = input_string.title()
    
    print("Title case version:", title_case_string)

# Call the function to execute the program
convert_to_title_case()




















